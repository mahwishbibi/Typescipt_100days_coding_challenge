// **Question 61:** Making Enums for Vehicles: Let's create categories for vehicles like cars, trucks, and motorcycles using enums, and show one example.

enum VehicleCategory {
    Car = "Car",
    Truck = "Truck",
    Motorcycle = "Motorcycle",
  }
  
  // Example usage:
  const myVehicle: VehicleCategory = VehicleCategory.Car;
  const myFatherVehicle: VehicleCategory = VehicleCategory.Car;
  const myBrotherVehicle: VehicleCategory = VehicleCategory.Car;
  
  console.log(`My vehicle belongs to the category: ${myVehicle}`);
  console.log(`My father vehicle belongs to the category: ${myFatherVehicle}`);
  console.log(`My Brother vehicle belongs to the category: ${myBrotherVehicle}`);
  